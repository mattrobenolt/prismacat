const std = @import("std");
const Io = std.Io;
const Build = std.Build;
const mem = std.mem;
const Allocator = mem.Allocator;
const parseInt = std.fmt.parseInt;

const Color = struct {
    r: u8,
    g: u8,
    b: u8,
};

const ParsedTheme = struct {
    name: []const u8,
    stops: []const Color,
};

const color_keys = [_][]const u8{
    "red",
    "yellow",
    "green",
    "cyan",
    "blue",
    "purple",
    "brightRed",
    "brightYellow",
    "brightGreen",
    "brightCyan",
    "brightBlue",
    "brightPurple",
    "cursorColor",
    "selectionBackground",
};

const fallback_keys = [_][]const u8{
    "brightRed",
    "brightYellow",
    "brightGreen",
    "brightCyan",
    "brightBlue",
    "brightPurple",
};

pub fn module(b: *Build) *Build.Module {
    const schemes = b.dependency("iterm2_color_schemes", .{});
    const generated_file = writeGeneratedThemes(b, schemes.path("windowsterminal"));
    return b.createModule(.{ .root_source_file = generated_file });
}

fn writeGeneratedThemes(b: *Build, schemes_path: Build.LazyPath) Build.LazyPath {
    const contents = generate(b, schemes_path) catch @panic("failed to generate themes");
    const write_files = b.addWriteFiles();
    return write_files.add("generated_themes.zig", contents);
}

fn generate(b: *Build, schemes_path: Build.LazyPath) ![]const u8 {
    var arena_state: std.heap.ArenaAllocator = .init(b.allocator);
    defer arena_state.deinit();

    const allocator = arena_state.allocator();
    const io = b.graph.io;
    const path = try schemes_path.getPath4(b, null);

    var dir = try path.root_dir.handle.openDir(io, path.sub_path, .{ .iterate = true });
    defer dir.close(io);

    var file_names: std.ArrayList([]const u8) = .empty;

    var it = dir.iterate();
    while (try it.next(io)) |entry| {
        if (entry.kind != .file or !mem.endsWith(u8, entry.name, ".json")) continue;
        try file_names.append(allocator, try allocator.dupe(u8, entry.name));
    }

    mem.sort([]const u8, file_names.items, {}, lessThanString);

    var writer: Io.Writer.Allocating = .init(allocator);
    errdefer writer.deinit();

    try writer.writer.writeAll(
        \\// Generated by src/build/Themes.zig from mbadolato/iTerm2-Color-Schemes.
        \\// Do not edit by hand.
        \\
        \\pub const Color = struct {
        \\    r: u8,
        \\    g: u8,
        \\    b: u8,
        \\};
        \\
        \\pub const Theme = struct {
        \\    name: []const u8,
        \\    stops: []const Color,
        \\};
        \\
        \\
    );

    var parsed_themes: std.ArrayList(ParsedTheme) = .empty;

    for (file_names.items) |file_name| {
        const source = dir.readFileAlloc(io, file_name, allocator, .limited(1 << 20)) catch continue;
        const theme = parseTheme(allocator, source) catch continue;
        try parsed_themes.append(allocator, theme);

        try writer.writer.print("const stops_{d} = [_]Color{{\n", .{parsed_themes.items.len - 1});
        for (theme.stops) |color| {
            try writer.writer.print("    .{{ .r = {d}, .g = {d}, .b = {d} }},\n", .{ color.r, color.g, color.b });
        }
        try writer.writer.writeAll("};\n\n");
    }

    try writer.writer.writeAll("pub const themes = [_]Theme{\n");
    for (parsed_themes.items, 0..) |theme, index| {
        try writer.writer.writeAll("    .{ .name = ");
        try writeZigString(&writer.writer, theme.name);
        try writer.writer.print(", .stops = &stops_{d} }},\n", .{index});
    }
    try writer.writer.writeAll("};\n");

    return b.allocator.dupe(u8, writer.written());
}

fn lessThanString(_: void, lhs: []const u8, rhs: []const u8) bool {
    return mem.order(u8, lhs, rhs) == .lt;
}

fn parseTheme(allocator: Allocator, source: []const u8) !ParsedTheme {
    const name = try extractString(allocator, source, "name");
    errdefer allocator.free(name);

    const stops = try buildStops(allocator, source, name);
    errdefer allocator.free(stops);

    return .{ .name = name, .stops = stops };
}

fn buildStops(allocator: Allocator, source: []const u8, name: []const u8) ![]Color {
    var colors: std.ArrayList(Color) = .empty;
    defer colors.deinit(allocator);

    for (color_keys) |key| {
        const color = extractColor(allocator, source, key) catch continue;
        if (!isUsefulStop(color) or containsNear(colors.items, color)) continue;
        try colors.append(allocator, color);
    }

    if (colors.items.len < 4) {
        colors.clearRetainingCapacity();
        for (fallback_keys) |key| {
            const color = try extractColor(allocator, source, key);
            if (containsNear(colors.items, color)) continue;
            try colors.append(allocator, color);
        }
    }

    mem.sort(Color, colors.items, {}, lessThanHue);

    const stop_count = @min(colors.items.len, 8);
    const stops = try allocator.alloc(Color, stop_count);
    const offset = if (colors.items.len == 0) 0 else std.hash.Wyhash.hash(0, name) % colors.items.len;
    for (stops, 0..) |*stop, index| {
        stop.* = colors.items[(offset + index * colors.items.len / stop_count) % colors.items.len];
    }
    return stops;
}

fn isUsefulStop(color: Color) bool {
    const min_channel = @min(color.r, @min(color.g, color.b));
    const max_channel = @max(color.r, @max(color.g, color.b));
    return max_channel >= 64 and max_channel - min_channel >= 28;
}

fn containsNear(colors: []const Color, candidate: Color) bool {
    for (colors) |color| {
        if (distance(color, candidate) < 42) return true;
    }
    return false;
}

fn distance(a: Color, b: Color) u16 {
    return absDiff(a.r, b.r) + absDiff(a.g, b.g) + absDiff(a.b, b.b);
}

fn absDiff(a: u8, b: u8) u16 {
    return if (a > b) a - b else b - a;
}

fn lessThanHue(_: void, lhs: Color, rhs: Color) bool {
    const lhs_hue = hue(lhs);
    const rhs_hue = hue(rhs);
    if (lhs_hue == rhs_hue) return saturation(lhs) > saturation(rhs);
    return lhs_hue < rhs_hue;
}

fn hue(color: Color) f32 {
    const r: f32 = @floatFromInt(color.r);
    const g: f32 = @floatFromInt(color.g);
    const b: f32 = @floatFromInt(color.b);
    const max_channel = @max(r, @max(g, b));
    const min_channel = @min(r, @min(g, b));
    const delta = max_channel - min_channel;
    if (delta == 0) return 0;

    const raw = if (max_channel == r)
        @mod((g - b) / delta, 6)
    else if (max_channel == g)
        (b - r) / delta + 2
    else
        (r - g) / delta + 4;

    return raw * 60;
}

fn saturation(color: Color) u8 {
    const min_channel = @min(color.r, @min(color.g, color.b));
    const max_channel = @max(color.r, @max(color.g, color.b));
    return max_channel - min_channel;
}

fn extractString(allocator: Allocator, source: []const u8, key: []const u8) ![]const u8 {
    const key_index = mem.find(u8, source, key) orelse return error.MissingKey;
    const after_key = source[key_index + key.len ..];
    const colon_index = mem.findScalar(u8, after_key, ':') orelse return error.BadJson;
    const after_colon = after_key[colon_index + 1 ..];
    const start_quote = mem.findScalar(u8, after_colon, '"') orelse return error.BadJson;
    const value_start = start_quote + 1;
    const value_end = mem.findScalar(u8, after_colon[value_start..], '"') orelse return error.BadJson;
    return allocator.dupe(u8, after_colon[value_start..][0..value_end]);
}

fn extractColor(allocator: Allocator, source: []const u8, key: []const u8) !Color {
    const value = try extractString(allocator, source, key);
    defer allocator.free(value);

    if (value.len != 7 or value[0] != '#') return error.BadColor;
    return .{
        .r = try parseInt(u8, value[1..3], 16),
        .g = try parseInt(u8, value[3..5], 16),
        .b = try parseInt(u8, value[5..7], 16),
    };
}

fn writeZigString(writer: *Io.Writer, bytes: []const u8) Io.Writer.Error!void {
    try writer.writeAll("\"");
    for (bytes) |byte| switch (byte) {
        '\\' => try writer.writeAll("\\\\"),
        '"' => try writer.writeAll("\\\""),
        '\n' => try writer.writeAll("\\n"),
        '\r' => try writer.writeAll("\\r"),
        '\t' => try writer.writeAll("\\t"),
        else => try writer.print("{c}", .{byte}),
    };
    try writer.writeAll("\"");
}
